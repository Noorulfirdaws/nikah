export function getT(lang: string): Translations { return LANGS[lang as LangCode] ?? LANGS.EN }
export function applyLangToDocument(lang: string) {
  const code = lang as LangCode
  document.documentElement.lang = code.toLowerCase()
  // Do NOT flip dir on <html> — it mirrors the entire layout.
  // Arabic/Urdu text renders correctly without it (browsers handle inline RTL text natively).
  document.documentElement.dir = 'ltr'
}
